class Operators {
    public static void main(String[] args) {
        /*
         * int number1 = 12;
         * int number2 = 6;
         * 
         * // addition
         * System.out.println(number1 + number2);
         * 
         * // subtraction
         * System.out.println(number1 - number2);
         * 
         * // multiplication
         * System.out.println(number1 * number2);
         * 
         * // division , no remainder unless double present
         * // if int divided by double or float vise versa, always another double
         * System.out.println(number1 / number2);
         * 
         * // remainder (modulo/modulus)
         * System.out.println(number1 % number2);
         */

        int number1 = 12;
        int number2 = 15;

        // is equal to
        System.out.println(number1 == number2);

        // is not equal to
        System.out.println(number1 != number2);

        // is greater than
        System.out.println(number1 > number2);

        // is less than
        System.out.println(number1 < number2);

        // is greater than or equal to
        System.out.println(number1 >= number2);

        // is less than or equal to
        System.out.println(number1 <= number2);

    }
}
